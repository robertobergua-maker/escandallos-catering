-- 3) Validaciones previas.
-- Debe devolver 0 filas: códigos nuevos duplicados en el mapeo.
select codigo_nuevo, count(*) as veces
from tmp_recodificacion_inventario
group by codigo_nuevo
having count(*) > 1;

-- Debe devolver 0 filas: códigos actuales del mapeo que no existen en inventario.
select m.codigo_actual
from tmp_recodificacion_inventario m
left join public.inventario i on i.codigo = m.codigo_actual
where i.codigo is null;

-- Debe devolver 0 filas: códigos nuevos que ya existen en inventario y no son el mismo registro.
select m.codigo_actual, m.codigo_nuevo
from tmp_recodificacion_inventario m
join public.inventario i on i.codigo = m.codigo_nuevo
where m.codigo_actual <> m.codigo_nuevo;

-- 4) Insertar clones con el código nuevo.
-- Esto evita errores si receta_ingredientes tiene FK hacia inventario.codigo sin ON UPDATE CASCADE.
insert into public.inventario (
    codigo,
    familia,
    descripcion,
    merma,
    precio_unidad,
    proveedor_precio,
    formato_compra,
    precio_original,
    unidad_original,
    fecha_precio,
    url_precio,
    observaciones_precio,
    unidad_medida,
    cantidad_formato_compra,
    unidad_formato_compra,
    precio_formato_compra
)
select
    m.codigo_nuevo as codigo,
    coalesce(nullif(m.familia_nombre_nueva, ''), i.familia) as familia,
    i.descripcion,
    i.merma,
    i.precio_unidad,
    i.proveedor_precio,
    i.formato_compra,
    i.precio_original,
    i.unidad_original,
    i.fecha_precio,
    i.url_precio,
    i.observaciones_precio,
    i.unidad_medida,
    i.cantidad_formato_compra,
    i.unidad_formato_compra,
    i.precio_formato_compra
from public.inventario i
join tmp_recodificacion_inventario m on m.codigo_actual = i.codigo
where not exists (
    select 1
    from public.inventario destino
    where destino.codigo = m.codigo_nuevo
);

-- 5) Actualizar referencias de recetas a los códigos nuevos.
update public.receta_ingredientes ri
set codigo_ingrediente = m.codigo_nuevo
from tmp_recodificacion_inventario m
where ri.codigo_ingrediente = m.codigo_actual;

-- 6) Borrar códigos antiguos de inventario ya migrados.
-- Solo borra antiguos que tienen clon nuevo y que ya no están referenciados.
delete from public.inventario i
using tmp_recodificacion_inventario m
where i.codigo = m.codigo_actual
  and exists (
      select 1
      from public.inventario nuevo
      where nuevo.codigo = m.codigo_nuevo
  )
  and not exists (
